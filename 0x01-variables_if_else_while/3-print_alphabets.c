#include <stdio.h>

#include <stdlib.h>

/**
 * main - Entry point
 *
 * Description: 'see here'
 *
 * Return: 0 (Success)
 */

int main(void)

{

		char c = 'a';



		while (c <= 'z')

		{

		putchar(c);

		c++;

	        }



	        c = 'A';



		while (c <= 'Z')

		{

		putchar(c);

		c++;

		}

		putchar('\n');

		return (0);

}
